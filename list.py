# 2022.12.13[프뚜]: 배열 길이
list = [1, 2, 3, 4, 5]
len(list)

# 2022.12.13[프뚜]: 값 변경
list = [1, 2, 3, 4, 5]
list[0] = 6 # [6, 2, 3, 4, 5]

# 2022.12.13[프뚜]: index번째 값 제거
