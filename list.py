
# 2022.12.13[프뚜]: 배열
list = [1, 2, 3, 4, 5]

# 2022.12.13[프뚜]: 값 조회
list[0] # 1
list[0:2] # [1, 2]
list[2:] # [3, 4, 5]
list[:2] # [1, 2]

# 2022.12.13[프뚜]: 배열 연결
list1 = [1, 2]
list2 = [3, 4, 5]
list1 + list2 # [1, 2, 3, 4, 5]

list1 = [1, 2]
list2 = [3, 4, 5]
list1.extend(list2) # [1, 2, 3, 4, 5]

# 2022.12.13[프뚜]: 배열 복사
